	ORG	00H		; PROGRAM STORE LOCATION
	MOV	P0,#00H		; REFRESHING PORT-0
	MOV	P1,#00H		; REFRESHING PORT-1
	MOV	P2,#00H		; REFRESHING PORT-2
	MOV	P3,#00H		; REFRESHING PORT-3

AGAIN:	
	MOV	P2,#00000001B
	LCALL   DELAY		; CALL DELAY 
	MOV	P2,#00000010B
	LCALL   DELAY		; CALL DELAY 
	MOV	P2,#00000100B
	LCALL   DELAY		; CALL DELAY 
	MOV	P2,#00001000B
	LCALL   DELAY		; CALL DELAY 
	MOV	P2,#00010000B
	LCALL   DELAY		; CALL DELAY 
	MOV	P2,#00100000B
	LCALL   DELAY		; CALL DELAY 
	MOV	P2,#01000000B
	LCALL   DELAY		; CALL DELAY 
	MOV	P2,#10000000B
	LCALL   DELAY		; CALL DELAY 

	LJMP	AGAIN		; LOOP FOREVER TO LABEL AGAIN


DELAY:				; DELAY SUBROUTINE 
	MOV	R7,#20		;		
L3 :	MOV	R6,#100		;
L2 :	MOV	R5,#100		;
L1 :	DJNZ	R5,L1		;
	DJNZ	R6,L2		;
	DJNZ	R7,L3		;
	RET			; END DELAY SUBROUTINE

	END			; END OF PROGRAM